package com.tramate.dto;

public class ScheduleDto {

	private int num;
	private int gnum;
	private int tnum;
	private int pks;
	private int type;
	
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getGnum() {
		return gnum;
	}
	public void setGnum(int gnum) {
		this.gnum = gnum;
	}
	public int getTnum() {
		return tnum;
	}
	public void setTnum(int tnum) {
		this.tnum = tnum;
	}
	public int getPks() {
		return pks;
	}
	public void setPks(int pks) {
		this.pks = pks;
	}
	public int getType() {
		return type;
	}
	public void setType(int type) {
		this.type = type;
	}
	
}
